package com.JavaReviews;

public class LoopsReviewAwet {
	
	public static void main(String[] args) {
		/*Write a program that prints starting at 18 decreasing in multiples of 3. (18,15,12,9,6,3)
		 * usinf for loop, while loop, & Do while loop
		 */
		
		//for loop
		for (int i=18; i>=1; i-=3) {
			System.out.println(i);
		}
		
		//while loop 
		int b =18;
		while(b>=1) {
			System.out.println(b);
			b-=3;
		}
		
		//Do while
		int c=18;
		do{
			System.out.println(c);
			c-=3;
		}while (c>=1);
		
		
	
		}
}




